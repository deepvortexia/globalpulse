// Runs an async mapper over items with a fixed concurrency ceiling. Shared by
// the cron's RSS-fetch and Haiku-classification stages, both of which need to
// bound how many in-flight requests hit an upstream source at once.
export async function mapPool<T, R>(
  items: T[],
  limit: number,
  fn: (item: T) => Promise<R>,
): Promise<R[]> {
  const results: R[] = new Array(items.length);
  let cursor = 0;
  async function worker() {
    while (cursor < items.length) {
      const index = cursor++;
      results[index] = await fn(items[index]);
    }
  }
  await Promise.all(Array.from({ length: Math.min(limit, items.length) }, worker));
  return results;
}
